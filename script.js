let leftOperand;
let rightOperand;

function add(leftOperand, rightOperand) {
    return leftOperand + rightOperand;
};

function subtract(leftOperand, rightOperand) {
    return leftOperand - rightOperand;
}

function multiply(leftOperand, rightOperand) {
    return leftOperand * rightOperand;
}

function divide(leftOperand, rightOperand) {
    return leftOperand / rightOperand;
}

